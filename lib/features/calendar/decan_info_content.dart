/// Lookup by short decan label (e.g., 'sbꜣw').
String? decanInfoForLabel(String decanLabel) {
  for (final entry in DecanMetadata.decanNames.entries) {
    final month = entry.key;
    final names = entry.value;
    for (int i = 0; i < names.length; i++) {
      if (names[i] == decanLabel) {
        final idx = (month - 1) * 3 + i;
        if (idx >= 0 && idx < decanInfoContent.length) {
          return decanInfoContent[idx];
        }
      }
    }
  }
  return null;
}
